
public class Swap {

	public static void main(String[] args) {
		int x, y, temp1;
		x = 10;
		y = 5;
		temp1 = x;
		x = y;
		y = temp1;
		System.out.println("x:"+x+" y:"+y);
		System.out.println();
	//
		double z, w, temp2; 
		z = 2.5; 
		w = 7.5;
		temp2 = z;
		z = w;
		w = temp2;
		System.out.println("x:"+z+" y:"+w);
		System.out.println();
	//
		int a, b, c, temp3; 
		a = 10; 
		b = 5;
		c = 12;
		temp3 = a;
		a = c;
		c = b;
		b = temp3;
		System.out.println("x:"+a+" y:"+b+" z:"+c);
		System.out.println();


	}

}
